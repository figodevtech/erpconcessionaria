alter table public.transactions
  add column if not exists categoria_id integer null,
  add column if not exists banco_id bigint null,
  add column if not exists payment_method_id integer null;

do $$
begin
  if not exists (
    select 1 from pg_constraint where conname = 'transactions_categoria_id_fkey'
  ) then
    alter table public.transactions
      add constraint transactions_categoria_id_fkey
      foreign key (categoria_id) references public.transaction_categories (id)
      on update cascade;
  end if;

  if not exists (
    select 1 from pg_constraint where conname = 'transactions_banco_id_fkey'
  ) then
    alter table public.transactions
      add constraint transactions_banco_id_fkey
      foreign key (banco_id) references public.bank_accounts (id)
      on update restrict;
  end if;

  if not exists (
    select 1 from pg_constraint where conname = 'transactions_payment_method_id_fkey'
  ) then
    alter table public.transactions
      add constraint transactions_payment_method_id_fkey
      foreign key (payment_method_id) references public.payment_methods (id)
      on update cascade;
  end if;
end
$$;

create index if not exists idx_transactions_categoria_id on public.transactions using btree (categoria_id);
create index if not exists idx_transactions_banco_id on public.transactions using btree (banco_id);
create index if not exists idx_transactions_payment_method_id on public.transactions using btree (payment_method_id);

insert into public.permissions (slug, module, action, description)
select *
from (
  values
    ('types:view', 'types', 'view', 'Visualizar cadastros de tipos'),
    ('types:create', 'types', 'create', 'Criar cadastros de tipos'),
    ('types:update', 'types', 'update', 'Editar cadastros de tipos'),
    ('types:delete', 'types', 'delete', 'Excluir cadastros de tipos')
) as new_permissions(slug, module, action, description)
where not exists (
  select 1
  from public.permissions p
  where p.slug = new_permissions.slug
);

insert into public.role_permissions (role_name, permission_slug)
select 'Administrador', p.slug
from public.permissions p
where p.slug in ('types:view', 'types:create', 'types:update', 'types:delete')
and not exists (
  select 1
  from public.role_permissions rp
  where rp.role_name = 'Administrador'
    and rp.permission_slug = p.slug
);

do $$
begin
  if not exists (select 1 from pg_type where typname = 'banco_tipo') then
    create type public.banco_tipo as enum ('CORRENTE', 'POUPANCA', 'CAIXA', 'CARTAO', 'INVESTIMENTO', 'OUTRO');
  end if;
end
$$;

create table if not exists public.transaction_categories (
  id integer generated always as identity not null,
  nome text not null,
  descricao text null,
  ativo boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint transaction_categories_pkey primary key (id),
  constraint transaction_categories_nome_key unique (nome)
);

create table if not exists public.bank_accounts (
  id bigint generated by default as identity not null,
  titulo text not null,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  valor_inicial numeric(14, 2) not null default 0,
  agencia text null,
  conta_numero text null,
  tipo public.banco_tipo not null default 'CORRENTE'::public.banco_tipo,
  proprietario text null,
  ativo boolean not null default true,
  constraint bank_accounts_pkey primary key (id),
  constraint bank_accounts_titulo_key unique (titulo)
);

create table if not exists public.payment_methods (
  id integer generated always as identity not null,
  nome text not null,
  codigo text not null default 'OUTRO',
  descricao text null,
  ativo boolean not null default true,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  constraint payment_methods_pkey primary key (id),
  constraint payment_methods_nome_key unique (nome)
);

alter table public.transaction_categories enable row level security;
alter table public.bank_accounts enable row level security;
alter table public.payment_methods enable row level security;


from (
  values
    ('DOCUMENTO DO VEICULO', 'CRLV, DUT e documentos oficiais do veiculo'),
    ('COMPROVANTE', 'Comprovantes financeiros e recibos'),
    ('LAUDO', 'Laudos cautelares, vistorias e relatorios tecnicos'),
    ('CONTRATO', 'Contratos e termos assinados')
) as seed(nome, descricao)
where not exists (
  select 1 from public.document_categories c where c.nome = seed.nome
);


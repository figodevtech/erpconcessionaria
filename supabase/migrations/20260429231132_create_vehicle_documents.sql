insert into public.permissions (slug, module, action, description)
select *
from (
  values
    ('documents:view', 'documents', 'view', 'Visualizar anexos de veiculos'),
    ('documents:create', 'documents', 'create', 'Criar anexos de veiculos'),
    ('documents:update', 'documents', 'update', 'Editar anexos de veiculos'),
    ('documents:delete', 'documents', 'delete', 'Excluir anexos de veiculos')
) as new_permissions(slug, module, action, description)
where not exists (
  select 1
  from public.permissions p
  where p.slug = new_permissions.slug
);

insert into public.role_permissions (role_name, permission_slug)
select 'Administrador', p.slug
from public.permissions p
where p.slug in ('documents:view', 'documents:create', 'documents:update', 'documents:delete')
and not exists (
  select 1
  from public.role_permissions rp
  where rp.role_name = 'Administrador'
    and rp.permission_slug = p.slug
);
